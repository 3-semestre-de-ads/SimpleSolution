package controller;
/**
 * A classe "Responsável" permite a criação
 * do cadastro de resposável e seus requisitos.
 * @author Andressa, Ede, Leonardo, Natália.
 */

public class Responsavel {
private int codResp;
private String nomeResp;
private String rgResp;
private String cpfResp;
private String emailResp;
private String telefoneResp;
/**
 * @return o codResp
 */
private int getCodResp() {
	return codResp;
}
/**
 * @param codResp o codResp para set
 */
private void setCodResp(int codResp) {
	this.codResp = codResp;
}
/**
 * @return o nomeResp
 */
private String getNomeResp() {
	return nomeResp;
}
/**
 * @param nomeResp o nomeResp para set
 */
private void setNomeResp(String nomeResp) {
	this.nomeResp = nomeResp;
}
/**
 * @return o rgResp
 */
private String getRgResp() {
	return rgResp;
}
/**
 * @param rgResp o rgResp para set
 */
private void setRgResp(String rgResp) {
	this.rgResp = rgResp;
}
/**
 * @return o cpfResp
 */
private String getCpfResp() {
	return cpfResp;
}
/**
 * @param cpfResp o cpfResp para set
 */
private void setCpfResp(String cpfResp) {
	this.cpfResp = cpfResp;
}
/**
 * @return o emailResp
 */
private String getEmailResp() {
	return emailResp;
}
/**
 * @param emailResp o emailResp para set
 */
private void setEmailResp(String emailResp) {
	this.emailResp = emailResp;
}
/**
 * @return o telefoneResp
 */
private String getTelefoneResp() {
	return telefoneResp;
}
/**
 * @param telefoneResp o telefoneResp para set
 */
private void setTelefoneResp(String telefoneResp) {
	this.telefoneResp = telefoneResp;
}

}
